package beans;

public class AdminHighlightsBeans {
	
	public Integer Hid;
	public String Head;
	public String Desc;
	public String Hurl1;
	public String Hurl2;
	
	
	public Integer getHid() {
		return Hid;
	}
	public void setHid(Integer hid) {
		Hid = hid;
	}
	public String getHead() {
		return Head;
	}
	public void setHead(String head) {
		Head = head;
	}
	public String getDesc() {
		return Desc;
	}
	public void setDesc(String desc) {
		Desc = desc;
	}
	public String getHurl1() {
		return Hurl1;
	}
	public void setHurl1(String hurl1) {
		Hurl1 = hurl1;
	}
	public String getHurl2() {
		return Hurl2;
	}
	public void setHurl2(String hurl2) {
		Hurl2 = hurl2;
	}

}
